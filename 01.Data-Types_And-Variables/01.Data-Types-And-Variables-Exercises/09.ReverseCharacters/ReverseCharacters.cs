using System;

namespace _09.ReverseCharacters
{
    class ReverseCharacters
    {
        static void Main(string[] args)
        {
            char a = Convert.ToChar(Console.ReadLine());
            char b = Convert.ToChar(Console.ReadLine());
            char c = Convert.ToChar(Console.ReadLine());
            Console.Write(c);
            Console.Write(b);
            Console.WriteLine(a);
        }
    }
}
